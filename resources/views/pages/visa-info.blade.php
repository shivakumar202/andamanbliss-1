@push('scripts')
<script type="text/javascript">
$(document).ready(function() {
    // Initialize FAQ accordion functionality
    $('.faq-question').on('click', function() {
        $(this).attr('aria-expanded', function(i, attr) {
            return attr == 'true' ? 'false' : 'true';
        });

        // Toggle the collapse state
        $(this).next('.faq-answer').collapse('toggle');
    });

    // Smooth scroll to requirements section
    $('.scroll-to-requirements').on('click', function(e) {
        e.preventDefault();
        $('html, body').animate({
            scrollTop: $('.visa-requirements-section').offset().top - 100
        }, 800);
    });

    // Calculate total amount with processing fee
    $('#amount').on('input', function() {
        var amount = parseFloat($(this).val()) || 0;
        var processingFee = amount * 0.025; // 2.5% processing fee
        var totalAmount = amount + processingFee;
        $('#totalAmount').val('₹' + totalAmount.toFixed(2));
    });
});
</script>
