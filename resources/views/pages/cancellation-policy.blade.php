<script type="text/javascript">
$(document).ready(function() {
    // Smooth scrolling for anchor links
    $('.policy-nav a').on('click', function(e) {
        e.preventDefault();

        // Remove active class from all links
        $('.policy-nav a').removeClass('active');

        // Add active class to clicked link
        $(this).addClass('active');

        // Get the target section
        var target = $(this).attr('href');

        // Animate scroll to target
        $('html, body').animate({
            scrollTop: $(target).offset().top - 100
        }, 800);
    });

    // Highlight active section on scroll
    $(window).on('scroll', function() {
        var scrollPosition = $(window).scrollTop();

        // Check each section
        $('.policy-section').each(function() {
            var topDistance = $(this).offset().top - 120;

            if (scrollPosition >= topDistance) {
                var sectionId = $(this).attr('id');

                // Remove active class from all links
                $('.policy-nav a').removeClass('active');

                // Add active class to corresponding link
                $('.policy-nav a[href="#' + sectionId + '"]').addClass('active');
            }
        });
    });

    // Animate elements on scroll
    function animateOnScroll() {
        $('.policy-section, .policy-sidebar > div').each(function() {
            var elementPos = $(this).offset().top;
            var topOfWindow = $(window).scrollTop();
            var windowHeight = $(window).height();

            if (elementPos < topOfWindow + windowHeight - 100) {
                $(this).addClass('animate__animated animate__fadeInUp');
            }
        });
    }

    // Run animation on page load
    setTimeout(function() {
        animateOnScroll();
    }, 500);

    // Run animation on scroll
    $(window).on('scroll', function() {
        animateOnScroll();
    });
});
</script>
